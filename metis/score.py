def _build_prescreen_context() -> str:
    """Minimal profile context for pre-screening.

    Intentionally excludes seniority level and target role titles — the pre-screen
    only sees title+company (no JD), so it cannot reliably assess scope. A "Senior PM"
    role may be staff-scope in practice; filtering on level here causes false negatives.
    Only deal-breakers that are inferable from title+company alone are included.
    """
    try:
        from .profile import load_profile_yaml
        data = load_profile_yaml() or {}
    except Exception:
        return ""

    target_roles = data.get("target", {}).get("roles", [])
    # Use first target role as the function signal; fall back to PM for legacy profiles.
    function_hint = target_roles[0] if target_roles else "Product Management"

    # Derive the job function label for the filter instruction.
    # Be explicit about common confusable pairs so Haiku doesn't conflate them.
    fn_lower = function_hint.lower()
    if "designer" in fn_lower or "design" in fn_lower:
        function_label = "Product Design / UX Design"
        not_function = (
            "Product Manager, Program Manager, Data Scientist, Engineer, or any non-design role. "
            "NOTE: 'Product Designer' and 'Product Manager' are DIFFERENT functions — filter PM roles."
        )
    elif "engineer" in fn_lower or "ml" in fn_lower or "machine learning" in fn_lower:
        function_label = "Engineering / ML Engineering"
        not_function = "Product Manager, Data Analyst, or non-engineering roles"
    else:
        function_label = function_hint
        not_function = "completely unrelated functions (e.g., Finance, Legal, Marketing)"

    lines: list[str] = [
        f"Candidate is seeking: {function_label} roles (e.g. {function_hint}).",
        f"Filter ONLY if the role is clearly NOT {function_label} — i.e. it is a {not_function}.",
        "When in doubt, PASS the role through (do not over-filter).",
    ]

    deal_breakers = data.get("deal_breakers", [])
    if deal_breakers:
        lines.append("Hard no (flag if obviously matched by title/company): "
                     + "; ".join(str(d) for d in deal_breakers[:6]))

    return "\n".join(lines)


def estimate_cost(n: int) -> str:
    """Human-readable Sonnet cost range for scoring n jobs (upper bound before pre-screen).

    Exported so pipeline.py can reference this single source of truth for cost estimates.
    """
    lo, hi = n * 0.005, n * 0.015
    return f"${lo:.2f}–${hi:.2f}"


def estimate_cost_hi(n: int) -> float:
    """Upper-bound cost estimate as a float — used by the cost gate in pipeline.py."""
    return n * 0.015


def _recover_partial_json(raw: str) -> list[dict]:
    """Salvage individual job objects from a truncated JSON array.

    Walks the raw string tracking brace depth so each complete {...} block
    can be parsed independently, returning whatever objects are valid.
    """
    objects: list[dict] = []
    depth, start = 0, None
    for i, ch in enumerate(raw):
        if ch == "{":
            if depth == 0:
                start = i
            depth += 1
        elif ch == "}":
            depth -= 1
            if depth == 0 and start is not None:
                try:
                    obj = json.loads(raw[start : i + 1])
                    if isinstance(obj, dict):
                        objects.append(obj)
                except json.JSONDecodeError:
                    pass
                start = None
    return objects


def prescreen_jobs_batch(client: anthropic.Anthropic, jobs: list[dict]) -> list[dict]:
    """Fast Haiku pre-screen on title+company only — filters obvious mismatches before
    JD enrichment and full Sonnet scoring.

    Returns the subset of jobs worth scoring.  Falls back to the full list if
    the API call fails or the response cannot be parsed.
    """
    if not jobs:
        return jobs

    context = _build_prescreen_context()
    if not context:
        log.warning("Pre-screen: no profile context — skipping filter")
        return jobs

    job_lines = "\n".join(
        f"{i + 1}. {j['title']} at {j['company']}"
        for i, j in enumerate(jobs)
    )

    try:
        response = client.messages.create(
            model=PRESCREEN_MODEL,
            max_tokens=max(256, len(jobs) * 12),  # ~12 tok/job: "1. Y\n" with headroom
            system=(
                f"{context}\n\n"
                "Quick function filter. For each numbered job output only the number "
                "and Y or N — nothing else.\n"
                "N = the role is clearly the WRONG function for this candidate "
                "(see candidate target above). OR the title+company obviously matches "
                "a hard-no deal-breaker above.\n"
                "Y = the role is the RIGHT function or close enough to warrant a full JD review. "
                "Never filter on seniority level — scope must be assessed from the full JD, not the title.\n"
                "When uncertain, Y."
            ),
            messages=[{"role": "user", "content": job_lines}],
        )
    except Exception as exc:
        log.warning(
            "Pre-screen API call failed (%s) — skipping filter, proceeding with all %d roles. "
            "Estimated Sonnet cost: ~$%.4f",
            exc, len(jobs), estimate_cost(len(jobs)),
        )
        return jobs

    raw   = response.content[0].text.strip()
    usage = response.usage
    log.info(
        f"Pre-screen ({PRESCREEN_MODEL}) — "
        f"input: {usage.input_tokens} tok, output: {usage.output_tokens} tok"
    )

    # Parse lines like "1. Y", "2. N", "3 Y", "4) N", etc.
    passed:   list[dict] = []
    filtered: list[str]  = []
    matched:  set[int]   = set()
    for line in raw.splitlines():
        m = re.match(r"(\d+)[.):\s]+([YN])", line.strip(), re.IGNORECASE)
        if m:
            idx     = int(m.group(1)) - 1
            verdict = m.group(2).upper()
            if 0 <= idx < len(jobs) and idx not in matched:
                matched.add(idx)
                if verdict == "Y":
                    passed.append(jobs[idx])
                else:
                    filtered.append(f"{jobs[idx]['title']} at {jobs[idx]['company']}")

    if not passed and not filtered:
        log.warning(
            "Pre-screen: response couldn't be parsed — proceeding with all %d roles. "
            "Estimated Sonnet cost: ~$%.4f",
            len(jobs), estimate_cost(len(jobs)),
        )
        return jobs

    log.info("Pre-screen: %d/%d passed (%d filtered out)", len(passed), len(jobs), len(filtered))
    if filtered:
        log.info("Pre-screen filtered:\n  " + "\n  ".join(filtered))
    return passed


